export const loadDashboardOrders = () => {
  if (typeof window === "undefined") {
    return defaultDashboardOrders;
  }

  const storedOrders = window.localStorage.getItem(DASHBOARD_ORDERS_STORAGE_KEY);

  if (!storedOrders) {
    return defaultDashboardOrders;
  }

  try {
    const parsedOrders = JSON.parse(storedOrders);

    return defaultDashboardOrders.map((defaultOrder) => {
      const storedOrder = parsedOrders.find(
        (order) => order.id === defaultOrder.id,
      );

      return storedOrder ? { ...defaultOrder, ...storedOrder } : defaultOrder;
    });
  } catch {
    return defaultDashboardOrders;
  }
};

export const saveDashboardOrders = (orders) => {
  if (typeof window === "undefined") {
    return;
  }

  window.localStorage.setItem(
    DASHBOARD_ORDERS_STORAGE_KEY,
    JSON.stringify(orders),
  );
};
